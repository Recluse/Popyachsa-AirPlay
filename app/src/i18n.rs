/// Resolve the string table for a language (English fallback for the languages
/// not yet translated — they're filled incrementally).
pub fn s(lang: Lang) -> &'static Strings {
    use Lang::*;
    match lang {
        En => &EN, Ru => &RU, Uk => &UK, De => &DE, Fr => &FR, Es => &ES, It => &IT,
        PtBr => &PT_BR, Pl => &PL, Nl => &NL, Tr => &TR, ZhCn => &ZH_CN, Ja => &JA,
        Ko => &KO, Ar => &AR, Hi => &HI,
    }
}

