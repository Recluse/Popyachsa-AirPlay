//! Lightweight UI localization. 16 languages; auto-detected from the OS UI
//! language and overridable via Settings ("Language"). All operational UI
//! strings (tray menu, Settings labels/sections/buttons, About section titles)
//! are keyed in [`Strings`]; long About prose stays English for now.

/// Supported UI languages (16, matching the music-bot set).
#[derive(Debug, Clone, Copy, PartialEq, Eq)]
pub enum Lang {
    En, Ru, Uk, De, Fr, Es, It, PtBr, Pl, Nl, Tr, ZhCn, Ja, Ko, Ar, Hi,
}

impl Lang {
    /// All languages, in dropdown order (English first, then by native name).
    pub fn all() -> [Lang; 16] {
        use Lang::*;
        [En, Ru, Uk, De, Es, Fr, It, PtBr, Pl, Nl, Tr, ZhCn, Ja, Ko, Ar, Hi]
    }

    /// BCP-47-ish code persisted in config.
    pub fn code(self) -> &'static str {
        use Lang::*;
        match self {
            En => "en", Ru => "ru", Uk => "uk", De => "de", Fr => "fr", Es => "es",
            It => "it", PtBr => "pt-BR", Pl => "pl", Nl => "nl", Tr => "tr",
            ZhCn => "zh-CN", Ja => "ja", Ko => "ko", Ar => "ar", Hi => "hi",
        }
    }

    /// Endonym shown in the Language dropdown.
    pub fn native_name(self) -> &'static str {
        use Lang::*;
        match self {
            En => "English", Ru => "Русский", Uk => "Українська", De => "Deutsch",
            Fr => "Français", Es => "Español", It => "Italiano", PtBr => "Português (BR)",
            Pl => "Polski", Nl => "Nederlands", Tr => "Türkçe", ZhCn => "中文 (简体)",
            Ja => "日本語", Ko => "한국어", Ar => "العربية", Hi => "हिन्दी",
        }
    }

    /// Parse a config value: "auto" or a code; falls back to OS detection.
    pub fn from_config(value: &str) -> Lang {
        if value.is_empty() || value.eq_ignore_ascii_case("auto") {
            return Lang::detect();
        }
        Lang::all().into_iter().find(|l| l.code().eq_ignore_ascii_case(value))
            .unwrap_or_else(Lang::detect)
    }

    /// Detect from the OS UI *display* language (NOT the region/locale — a
    /// Russian-region machine with an English UI must get English). English
    /// fallback. Uses the primary-language part of the UI LANGID.
    pub fn detect() -> Lang {
        use Lang::*;
        match os_ui_primary_lang() & 0x3ff {
            0x09 => En, 0x19 => Ru, 0x22 => Uk, 0x07 => De, 0x0c => Fr, 0x0a => Es,
            0x10 => It, 0x16 => PtBr, 0x15 => Pl, 0x13 => Nl, 0x1f => Tr, 0x04 => ZhCn,
            0x11 => Ja, 0x12 => Ko, 0x01 => Ar, 0x39 => Hi,
            _ => En,
        }
    }
}

#[cfg(windows)]
fn os_ui_primary_lang() -> u16 {
    // GetUserDefaultUILanguage = the *display* language (Settings > Language),
    // unlike GetUserDefaultLocaleName which is the regional format.
    use windows::Win32::Globalization::GetUserDefaultUILanguage;
    unsafe { GetUserDefaultUILanguage() }
}
#[cfg(not(windows))]
fn os_ui_primary_lang() -> u16 { 0x09 }

